    public void getItems()
    {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] items = SqlQueries.getItems(SqlQueries.ITEM_TYPE);
        if(items==null)
        {
            JLabel message = new JLabel("SQL Error!");
            message.setFont(new Font("Tahoma",0,18));
            JOptionPane.showMessageDialog(null, message);
            jButton1MousePressed(null);
        }
        else
        {
            DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
            renderer.setHorizontalAlignment(JLabel.LEADING);
            jTable1.setRowSelectionAllowed(true);
            jTable1.setSelectionBackground(Color.blue);
            jTable1.setDefaultRenderer(Integer.class, renderer);
            jTable1.setDefaultRenderer(String.class, renderer);
            jTable1.setDefaultRenderer(Float.class, renderer);
            JTableHeader header = jTable1.getTableHeader();
            header.setFont(new Font("Tahoma",0,18));
            for(Object cur:items)
            {
                ItemObject temp = (ItemObject) cur;
                model.addRow(new Object[]{Integer.parseInt(temp.getID()), temp.getName(), Float.parseFloat(temp.getCost()), (Boolean.parseBoolean(temp.isActive()))?"YES":"NO"});
            }
            itemCount.setText(Integer.toString(model.getRowCount()));
        }
    }
